package com.example.hola.sintaxis

fun main() {
    val num1 = 2
    val num2 = 3

    val suma = num1 + num2
    val resta = num1 - num2
    val multiplicacion = num1 * num2
    val division = num1 / num2
    val modulo = num1 % num2

